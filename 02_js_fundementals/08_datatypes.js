// PRIMITIVE
// String
const name = 'John doe';
console.log(typeof name);
// Number 
const number = 12;
console.log(typeof number);
// boolean
const hasKids = true;
console.log(typeof hasKids);
// Null
const car = null;
console.log(typeof car);
// Undefined
let test;
console.log(typeof test);
// symbol
const sym = Symbol();
console.log(typeof sym);

